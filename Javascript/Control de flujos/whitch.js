// control de flujo switch

// caso 1
switch (1) {
    case 1: {
        console.log("yo soy el caso 1")
        break;
    }
    case 2:
        console.log("yo soy el caso 2")
        break;
    case 3:
        console.log("yo soy el caso 3")
        break
    default:
        console.log("yo soy el caso default")
}

